doc = Document()

# ---- Page margins ----
for section in doc.sections:
    section.top_margin    = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin   = Cm(3)
    section.right_margin  = Cm(1.5)

# ---- Default paragraph font ----
style = doc.styles['Normal']
style.font.name = 'Times New Roman'
style.font.size = Pt(14)

def set_font(run, bold=False, size=14, color=None):
    run.font.name = 'Times New Roman'
    run.font.size = Pt(size)
    run.bold = bold
    if color:
        run.font.color.rgb = RGBColor(*color)

def add_title(text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    set_font(run, bold=True, size=16)
    p.paragraph_format.space_after = Pt(12)

def add_subtitle(text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    set_font(run, bold=False, size=13, color=(80, 80, 80))
    p.paragraph_format.space_after = Pt(18)

def add_question(num, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(14)
    p.paragraph_format.space_after  = Pt(4)
    run = p.add_run(f"Вопрос {num}. {text}")
    set_font(run, bold=True, size=14, color=(0, 70, 127))

def add_answer_label():
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(2)
    run = p.add_run("Ответ:")
    set_font(run, bold=True, size=14)

def add_body(text):
    # strip markdown bold
    text = re.sub(r'\*\*(.*?)\*\*', r'\1', text)
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(1.25)
    p.paragraph_format.space_after = Pt(4)
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    run = p.add_run(text)
    set_font(run, size=14)

def add_bullet(text):
    text = re.sub(r'\*\*(.*?)\*\*', r'\1', text)
    p = doc.add_paragraph(style='List Bullet')
    p.paragraph_format.left_indent  = Cm(1.5)
    p.paragraph_format.space_after  = Pt(2)
    run = p.add_run(text)
    set_font(run, size=13)

def add_heading3(text):
    text = re.sub(r'\*\*(.*?)\*\*', r'\1', text)
    text = text.lstrip('#').strip()
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after  = Pt(3)
    run = p.add_run(text)
    set_font(run, bold=True, size=13, color=(60, 60, 60))

def add_divider():
    p = doc.add_paragraph()
    run = p.add_run('─' * 70)
    run.font.size = Pt(8)
    run.font.color.rgb = RGBColor(180, 180, 180)
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after  = Pt(6)

